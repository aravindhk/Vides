#include "cfree_cmatrix.h"
void cfree_cmatrix(complex **m, int nrl, int nrh,int ncl, int nch)
/* free a complex matrix allocated by dmatrix() */
{
	free((FREE_ARG) (m[nrl]+ncl-NR_END));
	free((FREE_ARG) (m+nrl-NR_END));
}
