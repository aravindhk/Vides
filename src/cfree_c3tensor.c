#include "cfree_c3tensor.h"
void cfree_c3tensor(complex ***t, int nrl, int nrh, int ncl, int nch,
		    int ndl, int ndh)
{
  free((FREE_ARG) (t[nrl][ncl]+ndl-NR_END));
  free((FREE_ARG) (t[nrl]+ncl-NR_END));
  free((FREE_ARG) (t+nrl-NR_END));
}
